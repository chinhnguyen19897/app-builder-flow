export const ProcessIcon = ({ total, values, icon }: any) => {
  const percentages = values.reduce(
    (acc: any, { value, color, label }: any) => {
      const pct = (value / total) * 100
      acc.values.push({
        value: pct,
        offset: 100 - acc.total + 25,
        color,
        label,
      })
      return {
        values: acc.values,
        total: acc.total + pct,
      }
    },
    { values: [], total: 0 }
  )

  return (
    <>
      <svg width={60} height={60} viewBox="0 0 40 40">
        <circle
          cx="20"
          cy="20"
          r="15.91549430918954"
          fill="transparent"
          stroke="#cccccc"
          strokeWidth={2}
        />
        {percentages.values.map(
          ({ value, offset, color, label }: any, index: number) => (
            <circle
              key={index}
              cx="20"
              cy="20"
              r="15.91549430918954"
              fill="transparent"
              strokeWidth={2}
              stroke={color}
              strokeDasharray={[value, 100 - value]}
              strokeDashoffset={offset}
            />
          )
        )}
      </svg>
      <span className="icon">{icon}</span>
    </>
  )
}
